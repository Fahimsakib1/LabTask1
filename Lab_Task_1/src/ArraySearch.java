public class ArraySearch {

	public static void main(String[] args)
	
	{
		
		String[] Item = { "Apple","Ball","Cat","Dog","Eagle"};
		String Search = "Dog";

		
		for(int i = 0; i < Item.length; i++)
		
		{

			if( Item[i]== Search )
			
			{

			System.out.println(" The  Search String is in  index : " +i);
			
			}
	
		}
	
	
	}

}
